package javapackage1;

public class ForLoopconcept {
	

	public static void main(String[] args) {
		for(int a=2;a<5;a++) 
		{
			System.out.println("My self Pavani");
		}
		for (byte i=9;i>5;i--)
		{
			System.out.println("welcome");
			
		}
		for(short k=1;k>0;k++)
		{
			System.out.println("India");
		}
			

	}

}
